// Ë¥≠Áâ©ËΩ¶ÂäüËÉΩÂ¢ûÂº∫
class CartItem {
    constructor(product, quantity = 1) {
        this.product = product;
        this.quantity = quantity;
    }

    get total() {
        return this.product.price * this.quantity;
    }
}

class Cart {
    constructor() {
        this.items = [];
    }

    addItem(product) {
        const existingItem = this.items.find(item => item.product.id === product.id);
        if (existingItem) {
            existingItem.quantity++;
        } else {
            this.items.push(new CartItem(product));
        }
        this.save();
        this.render();
    }

    removeItem(productId) {
        this.items = this.items.filter(item => item.product.id !== productId);
        this.save();
        this.render();
    }

    updateQuantity(productId, quantity) {
        const item = this.items.find(item => item.product.id === productId);
        if (item) {
            item.quantity = Math.max(1, quantity);
            this.save();
            this.render();
        }
    }

    get total() {
        return this.items.reduce((sum, item) => sum + item.total, 0);
    }

    save() {
        localStorage.setItem('cart', JSON.stringify(this.items));
    }

    load() {
        const savedCart = localStorage.getItem('cart');
        if (savedCart) {
            const parsedCart = JSON.parse(savedCart);
            this.items = parsedCart.map(item => new CartItem(item.product, item.quantity));
        }
    }

    render() {
        const cartItems = document.querySelector('.cart-items');
        const totalAmount = document.querySelector('.total-amount');
        
        cartItems.innerHTML = this.items.map(item => `
            <div class="cart-item">
                <img src="${item.product.image}" alt="${item.product.name}">
                <div class="item-details">
                    <h3>${item.product.name}</h3>
                    <p>Ôø•${item.product.price}</p>
                    <div class="quantity-controls">
                        <button onclick="cart.updateQuantity(${item.product.id}, ${item.quantity - 1})">-</button>
                        <span>${item.quantity}</span>
                        <button onclick="cart.updateQuantity(${item.product.id}, ${item.quantity + 1})">+</button>
                    </div>
                </div>
                <button onclick="cart.removeItem(${item.product.id})" class="remove-btn">Âà†Èô§</button>
            </div>
        `).join('');

        totalAmount.textContent = `Ôø•${this.total.toFixed(2)}`;
        updateCartTotal();
    }
}

