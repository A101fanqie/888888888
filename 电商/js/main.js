class CouponSystem {
    constructor() {
        // Á§∫‰æã‰ºòÊÉ†Âà∏Êï∞ÊçÆ
        this.coupons = {
            'WELCOME2024': { type: 'percentage', value: 10 }, // 10%ÊäòÊâ£
            'SAVE50': { type: 'fixed', value: 50 }, // Áõ¥Êé•Âáè50
            'NEWUSER': { type: 'percentage', value: 15 } // 15%ÊäòÊâ£
        };
        this.currentCoupon = null;
    }

    applyCoupon(code) {
        const coupon = this.coupons[code];
        if (!coupon) {
            throw new Error('Êó†ÊïàÁöÑ‰ºòÊÉ†Âà∏Á†Å');
        }
        this.currentCoupon = { ...coupon, code };
        return this.currentCoupon;
    }

    calculateDiscount(subtotal) {
        if (!this.currentCoupon) return 0;

        if (this.currentCoupon.type === 'percentage') {
            return subtotal * (this.currentCoupon.value / 100);
        } else {
            return Math.min(this.currentCoupon.value, subtotal);
        }
    }

    removeCoupon() {
        this.currentCoupon = null;
    }
}

